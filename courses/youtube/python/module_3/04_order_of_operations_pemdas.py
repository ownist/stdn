# order of operations (PEMDAS method) ----------------------

result = 3 + 4 * 2
print(result)

# PEMDAS method
# P -> Parentheses
# E -> Exponentiation / Exponents
# M -> Multiplication
# D -> Division
# A -> Addition
# S -> Substraction

pemdas = (4 + 3) + (2**3) * 3  # 7 + 8 * 3 -> 7 + (8 * 3) -> 7 + 24 -> 31
print(pemdas)

a = (3 + 2) * 8  # 5 * 8 -> 40
print(a)
