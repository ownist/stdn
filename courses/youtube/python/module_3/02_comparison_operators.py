# basic comparison operators ------------------------

# is equal to
print(5 == 5)  # True

# not equal to
print(2 != 2)  # False

# greater than
print(6 > 3)  # True

# less than
print(5 < 10)  # True

# greater than or equal (boro othoba soman)
print(10 >= 10)  # True bcz dosh 10 er soman but boro na

# less than or equal (choto othoba soman)
print(20 <= 50)  # True bcz 20 50 er theke choto
